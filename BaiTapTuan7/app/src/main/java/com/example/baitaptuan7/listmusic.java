package com.example.baitaptuan7;

public class listmusic {
    private String ten;
    private int file;

    public listmusic(String ten, int file) {
        this.ten = ten;
        this.file = file;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public int getFile() {
        return file;
    }

    public void setFile(int file) {
        this.file = file;
    }
}
